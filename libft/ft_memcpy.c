#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*s;
	unsigned char	*d;

	s = (unsigned char *)src;
	d = (unsigned char *)dest;
	if ((src == 0 && dest == 0) || !n)
		return (dest);
	while (n--)
		*(d++) = *(s++);
	return (dest);
}
