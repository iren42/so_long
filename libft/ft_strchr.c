#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	while (*str++)
	{
		if (c == *(str - 1))
			return ((char *)(str - 1));
	}
	if (c == '\0')
		return ((char *)(str - 1));
	return ((char *)0);
}
