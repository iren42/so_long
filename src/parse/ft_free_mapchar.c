#include "so_long.h"

void	ft_free_mapchar(char **map)
{
	int	i;

	i = 0;
	if (map)
	{
		while (map[i] != 0)
		{
			free(map[i]);
			map[i++] = 0;
		}
		free(map);
		map = 0;
	}
}
